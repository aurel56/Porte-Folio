    <p class="texte"><h3><center>><h2><b><u>Présentation du BTS SIO</u></b></h3></b> 


    <h3> Le BTS SIO prépare à deux métiers distincts, représentés par les deux spécialités du diplôme :<br><br>
    - Solutions d'infrastructure, systèmes et réseaux (SISR)<br>
    - Solutions logicielles et applications métiers (SLAM)<br><br>
    Le titulaire d'un BTS SISR est chargé d'installer, d'administrer et gérer la maintenance des<br> 
    équipements    et des réseaux informatiques. Il intervient au niveau de l'intégration, la <br> 
    sécurisation et la configuration des serveurs, des postes clients et des équipements <br>
    d'interconnexion. Il aura également la tâche d'anticiper les besoins d'évolution de l'infrastructure,<br>
    de maintenir la qualité des services informatiques et de proposer le cas échant des solutions<br>
    pour faire évoluer les services. <br>
<br>

<br>
    Le diplômé du BTS SIO Spécialité « Solutions logicielles et applications métiers »,<br>
    à la différence de la spécialité Solutions d'infrastructure, systèmes et réseaux, <br>
    va s'attacher à fournir des services orientés solutions logicielles et applicatives : <br>
    par exemple le développement de logiciels personnalisés de CRM (Customer Relationship<br> Management)
    ou de logiciels de réservations d'hôtels pour une chaîne hôtelière. <br><br>
    Il participera à toutes les phases de la production des solutions logicielles mises en place :<br>
    la définition du cahier des charges, en prenant en compte les besoins des utilisateurs finaux <br>
    et les contraintes propres à l'entreprise cliente,le choix des solutions techniques,<br>
    la réalisation des applications logicielles,<br>
    leur mise en place au sein de l'organisation, et l'assistance apportée aux utilisateurs.</h3>

