<p><center><h2>Cantine de l'école:</p></center> </p></h2><br> 

<h3><center>
        
        La  cantine est dans les locaux de l'école, les enfants ont juste à traverser le préau.<br>
        Les TPS-PS et les MS vont se coucher juste après le repas pour garder un rythme biologique <br>
        propre à l'endormissement. Ils mangent au premier service de 12h à 12h45 <br>
        Les élèves de GS jouent dans la cour si le temps le permet, sinon ils jouent <br>
        à des jeux calmes en intérieur.<br><br>
        
        Ils déjeunent de 12h45 à 13h30.<br>
        Ils ont 15 minutes de récréation avant de retourner en classe.<br>
        La mise en place de 2 services permet aux enfants de manger dans le calme.<br><br>
        
        Chaque ATSEM s'occupe  des enfants de sa classe afin de préserver une continuité <br>
        et de servir de repère aux enfants dont elles ont la charge. Elles sont aidées par <br>
        du personnel communal.<br><br>
        
        Les menus,sont conformes aux directives nationales et visés par une diététicienne . <br>
        Les repas sont préparés chaque jour par la cuisine du collège de Theix-Noyalo et livrés dans la matinée.<br>
        La municipalité privilégie les filières Bio et les producteurs locaux dès que c'est possible.<br></center></h3></div></br>
   
